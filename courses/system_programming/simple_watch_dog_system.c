#include <stdlib.h>		/* system */
#include <unistd.h>		/* sleep  */

/*******************************************************************************
                                Main Function
*******************************************************************************/  
int main (void)
{
	int ret_val = 0;
	
	while (0 == ret_val)
	{
		ret_val = system("echo Hi everyone, I am system");
		sleep(1); 
	}
	
	return (ret_val);
}





